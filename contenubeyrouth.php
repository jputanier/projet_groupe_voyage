                <p id="overview">
                    
                    Beyrouth (en arabe : بيروت / bayrūt) est la capitale du Liban et la ville la plus importante du pays. 

                    C'est une métropole qui compte environ 360 000 habitants en 2011. 

                    L'agglomération urbaine comporte entre 1,8 et 2 millions d'habitants9,10, soit près de 50 % de la population du pays. 
                    
                    La ville forme le gouvernorat (muhafazat) de Beyrouth, le seul qui ne soit pas divisé en districts. 

                    Elle abrite le siège du gouvernement. Bien qu'il soit fréquent de parler de Beyrouth pour désigner la ville et ses banlieues, il n'existe pas de telle entité officielle, plusieurs administrations définissant seulement, à des fins d'études, une Région métropolitaine de Beyrouth.
                    
                    La ville a été nommée capitale mondiale du livre 2009 par l'UNESCO. 
                    
                    Beyrouth a également été citée dans le New York Times comme première destination à visiter en 2009.
                    
                    Les habitants de Beyrouth sont les Beyrouthins et ses habitantes les Beyrouthines.  
                </p>
            
            
                <p id="cuisine">
                    Il était déjà fait mention dans la Bible que le pays de Canaan, qui est l’actuel Liban, proposait une gastronomie d’une grande finesse et d’une grande variété grâce à son merveilleux climat méditerranéen, qui lui permet d’avoir tout au long de l’année des fruits et légumes frais en abondance.

                    La cuisine libanaise s’est enrichie de saveurs nouvelles venues d'ailleurs et elle a, au fil du temps, affiné les plats basiques, d’origine montagnarde, pour atteindre un grand raffinement.
                    
                    Mezze est un ensemble de hors-d’œuvre différents pouvant aller de six (petit mezze) à plus de cent plats (mezze de mariage) déposés sur la table et dont on se sert en s’aidant du pain libanais.

                    Il comprend un choix savant des spécialités suivantes : le tabouleh (salade de persil haché, de blé dur concassé, de tomates, d’oignons et de menthe), le fattouche est l'autre salade vedette. C'est un mélange de légumes de saison : pourpier, laitue, radis, tomates, concombres, persil et menthe ; le batenjan, appelé également baba ghanouj ou metabal, est une purée d'aubergines souvent décorée de grains de grenade ; le houmous, une purée de pois chiches à la purée de sésame (tahin ou tahini), parfois agrémenté de pignons frits et de viande ; le kebbé, viande fraîche de mouton ou de veau, battue avec du blé concassé, assaisonnée et servie crue, frite ou grillée ; le labné, sorte de fromage crémeux semblable au yaourt, les rkakates sont des petits chaussons de pâte feuilletée garnis de fromage de chèvre, de crabe ou de viande.

                    Leurs cousins, fourrés de selk (blettes) ou de kechk (délicieux yaourt, fermenté, séché puis réduit en poudre), se nomment fatayers ou sambousecks, les warak enab, feuilles de vigne farcies, et le foul médammas sont des fèves à l’huile.

                    Un mezze plus élaboré comporte plusieurs sortes de viandes rôties, frites ou grillées, des poissons et des légumes.

                    Malgré la richesse et l'abondance de ces mezze, qui fournissent un régime équilibré et peuvent représenter un repas à part entière, la cuisine libanaise offre bien d’autres spécialités de plats qu’il est impossible de citer. Certains, très longs à préparer, ne figurent pas au menu des restaurants, mais se dégustent en famille.
                </p>
            
            
                <p id="streetart">
                    C’est l’un des premiers aperçus de la capitale pour les passagers qui atterrissent à Beyrouth, au Liban. 
                    
                    À quelques centaines de mètres de l’aéroport, un îlot de maisons aux façades multicolores occupent le bord de mer. 
                    
                    Il y a un an et demi, un homme d’affaires a monté le projet Ouzville. Le concept : inviter des artistes du monde entier pour redonner des couleurs à Ouzai, l'un des bidonvilles du sud de Beyrouth. 
                    
                    Un moyen de désenclaver le quartier et de montrer que le street art peut impacter la société.
                </p>
            
            
                <p id="thingstodo">
                    Visiter Beyrouth, c’est un peu comme visiter plusieurs villes, voir plusieurs pays en un ! 
                    
                    En une seule journée, vous passez des magasins de luxe, aux échoppes plus traditionnelles, de quartier chrétien au quartier musulman, puis arménien, de bâtiments tout neufs au bord de la marina aux bâtiments abandonnés, des graffiti, « Crimes Never Ends in Beirut» à des expo contemporaines où les pièces d’art sont faîtes en rouleau de pièce de monnaie, de cafés traditionnels aux bars à cocktail les plus tendances… 
                    
                    Bref Bienvenue à Beyrouth !

                    Un peu comme à Paris, à Londres et toutes ces Capitales, Beyrouth est composée de quartiers bien distincts, on en compte une douzaine il me semble. Tous ont une petite particularité, Gemmayzeh pour sortir, Hamra pour se balader, Saifi village pour chiner, Ashrafieh pour certains musées, Borj Hammoud le quartier arménien et ses bijouteries, la Corniche…

                    Une balade dans Beirut est assez typique de ces pays/ville en plein dynamisme, en pleine mutation. 
                    
                    Ce qu’on ressent dans Beyrouth c’est le contraste des nouvelles constructions imposantes tel que le nouveau souk, la mosquée El Amine –massivement construite sur les flancs de la Cathédrale Saint Georges – ;  les bâtiments de la marina, construit sur une ville « détruite » (si on regarde derrière les nouveaux bâtiments on voit les signes des affrontements passés). 
                    
                    Design, endroit bobo et culture font aussi partie intégrante de la vie Beyrouthine. 
                    
                    Au Liban, Beyrouth est surtout renommée pour faire la fête. 
                    
                    En 2 jours on se fait une bonne idée de la ville, mais on n’a pas assez de temps pour tester toutes les très bonnes adresses de restauration et de bar.

                    Exemple de choses à faire à Beyrouth : Manger un Manouché au petit déjeuner ,Se baigner dans la piscine de l’Hôtel Gray et admirer le point de vue, Aller dans le quartier du parlement (et se retrouver face au barrage de barbelé).

                </p>
           
                <p id="nightlife">
                    Beyrouth est une ville dynamique et chaleureuse. 
                    
                    La ville est réputée pour sa vie nocturne, elle héberge de nombreuses discothèques, boîtes de nuit et pubs dans les quartiers Monnot, Verdun, Hamra, Gemmayzé et sur les corniches du bord de mer. 
                    
                    À Hamra, la rue comporte une multitude de restaurants, de cafés et de boutiques. 
                    
                    Les boîtes de nuits et discothèques sont aussi nombreuses sur la rue Monot. La corniche de Beyrouth, longeant la mer, est fréquentée par des promeneurs à n’importe quelle heure du jour ou de la nuit. 
                    
                    Fumer est très commun à Beyrouth, les restaurants ont des zones fumeurs, assurez-vous de demander si un restaurant est fumeur ou non, et de demander une table non-fumeur si vous ne voulez pas vous asseoir autour de fumeurs.
