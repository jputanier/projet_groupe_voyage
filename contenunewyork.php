                <p id="color" id="overview">
                New York, officiellement nommée City of New York, connue également sous les noms et abréviations de New York City ou NYC, est la plus grande ville des États-Unis en termes d'habitants et l'une des plus importantes du continent américain. 
                
                Elle se situe dans le Nord-Est des États-Unis, sur la côte atlantique, à l'extrémité sud-est de l'État de New York. 
                
                La ville de New York se compose de cinq arrondissements appelés boroughs : Manhattan, Brooklyn, Queens, le Bronx et Staten Island. Ses habitants s'appellent les New-Yorkais (en anglais : New Yorkers).
                
                New York accueille quelque 50 millions de visiteurs annuellement.
                
                Times Square, « The Crossroads of the World », est l'une des intersections les plus populaires du monde, et le quartier des théâtres de Broadway est la plaque tournante du spectacle dans le pays tout entier et un centre majeur de l'industrie du divertissement dans le monde15. 
                
                La ville abrite un grand nombre de ponts et tunnels (789 en 2012), gratte-ciel et parcs de renommée mondiale.

                New York se place en tête dans la triade des grands centres financiers mondiaux avec Londres et Hong Kong, ces trois villes étant appelées par les médias anglophones « Nylonkong ». 
                
                Le quartier financier de New York, ancré par Wall Street dans le Lower Manhattan, fonctionne ainsi comme la « capitale financière du monde » et est le foyer du New York Stock Exchange (Bourse de New York), tandis que le nouveau One World Trade Center est le plus haut gratte-ciel d'Amérique du Nord. 
                
                De plus, le marché immobilier de Manhattan est parmi les plus chers dans le monde. 
                </p>

                <p id="color" id="cuisine">
                Dire que les Américains mangent mal et trop est très simpliste. 
                
                C'est encore malheureusement une réalité dans certains coins des États-Unis (de moins en moins cela dit), mais pas à New York qui a toujours été un cas à part et où vous ferez de vraies découvertes culinaires, à condition d’aller dans les bons endroits, bien sûr.

                La capitale gastronomique des États-Unis (so foody, comme ils disent ici) se targue d'être aussi la « ville la plus mince » du pays. Les New-Yorkais pratiquent avec conviction le régime eating healthy et sont fans de bio (organic).
                
                Le phénomène « locavore » fait désormais partie des mœurs. Ce comportement alimentaire, né à San Francisco, privilégie la consommation d'ingrédients locaux, pas nécessairement bio mais produits dans un rayon limité (en général, une centaine de miles). L’équivalent de ce que l’on nomme circuit court ou kilomètre zéro en France.

                Mais la grande particularité de New York, c'est que toutes les cuisines sont représentées et qu'on trouve partout de tout à tous les prix, de la cuisine de rue (street food), vendue par les marchands ambulants (food carts) et les food trucks gourmets à la New American cuisine, concoctée par des chefs inspirés, en passant par les delis et les salad bars dans les grandes surfaces. Vous remarquerez rapidement que plus la cuisine est raffinée et créative, plus les quantités diminuent dans l'assiette (et plus l'addition est élevée, bien sûr) !

                Dans les restos, vous serez sans doute surpris par l’impressionnant volume sonore. Autre caractéristique, l’éclairage tamisé qui diminue à mesure que la soirée avance à tel point qu’on ne voit pas toujours très bien ce qu’on a dans l’assiette...
                
                Le manque de vraies places assises dans les petites adresses du midi ou les pâtisseries (en dehors de trois ou quatre tabourets hauts alignés le long d’un comptoir) étonnera aussi les visiteurs, pour qui la pause déjeuner est aussi l’occasion de s'asseoir dans cette ville où l’on marche tant (et où il y a en plus si peu de bancs dans les rues). Ce n’est pas dans la culture new-yorkaise de s’installer confortablement à l’heure du lunch.

                Un filon pour se faire une table de chef à moindres frais : la Restaurant Week. Deux fois dans l’année, en janvier-février et en juillet-août, un certain nombre de restaurants haut de gamme proposent, une semaine durant, des menus à 26 $ le midi et environ 42 $ le soir.
                </p>

            
                <p id="color" id="streetart">
                Le street art à New York, c’est toute une histoire, une religion presque dans cet immense temple de l’art urbain. 
                
                On ne compte plus la quantité et la qualité des œuvres qui habillent les façades. 
                
                La ville recèle de trésors graphiques. 
                
                Certaines sont là depuis longtemps, d’autres encore fraîches de la veille, car les artistes défilent, le street art se déplace et change au fil des années. 
                
                On vous propose une balade street art pour découvrir la ville autrement.
                </p>
            

                <p id="color" id='thingstodo'>
                Comme tout new-yorkais vous le dira, il est impossible de découvrir tous les meilleurs endroits de cette grande et complexe ville, en une seule visite. 
                
                Et oui, cela fait partie de l’excitation à visiter New York : il y a toujours quelque chose de nouveau à découvrir. 
                
                Mais toutes ces options alléchantes peuvent aussi rendre difficile la planification de votre visite de New York. 
                
                Alors au lieu d’essayer de tout voir, manger, boire et visiter à NYC, nous avons dressé une liste des principales meilleures choses à faire à New York : des sites emblématiques aux favoris des locaux.
                
                Exemple de choses à faire à New York : visiter La Statue de la Liberté, Central Park, Times Square.
                </p>


