                <p id="color" id='overview'>
                San Francisco, officiellement ville et comté de San Francisco (en anglais : City and County of San Francisco), est une ville américaine et un comté de l'État de Californie. 
                
                Elle est située à l'extrémité nord de la péninsule de San Francisco, entre l'océan Pacifique à l'ouest et la baie de San Francisco à l'est. 
                
                Son nom est couramment abrégé en SF et la ville est surnommée The City by the Bay.

                Fondée en 1776 par des Espagnols au sein de la vice-royauté de la Nouvelle-Espagne, la ville, nommée en l'honneur de San Francisco de Asís, prend son essor lors de la ruée vers l'or et son prolongement, l'embellissement de San Francisco par les millionnaires du Nevada. 
                
                Puis, elle devient le berceau du jeans avec la fondation de Levi Strauss & Co. 
                
                Les années 1950 voient la naissance de la Beat Generation. 
                
                À partir de la deuxième partie du XXe siècle, l'industrie des hautes technologies se développe dans la région de la baie. 
                
                Aujourd'hui San Francisco est la ville la plus densément peuplée des États-Unis après New York. 
                
                La municipalité-comté de San Francisco compte 884 363 habitants dans ses limites administratives et 7,8 millions de personnes vivent dans l'aire métropolitaine de La Baie, la quatrième métropole des États-Unis par sa population. 
                
                La partie sud de cette dernière est occupée par la municipalité de San José et la Silicon Valley, premier pôle de hautes technologies du monde qui accueille un nombre important d'entreprises de technologie de pointe de renommée mondiale telles que Cisco, Apple, Tesla Motors, Hewlett-Packard, Google, Intel ou encore Facebook7. 
                
                Dans le domaine universitaire, elle accueille les prestigieuses université Stanford et université de Californie à Berkeley. 
                
                San Francisco est également le siège de la Wikimedia Foundation dont fait partie le projet Wikipédia, et Internet Archive. 
                
                Au nord s'étendent la Napa Valley et la Sonoma Valley, renommées pour leur viticulture. San Francisco fait partie des villes progressistes dans le domaine de l'écologie et du développement durable.

                Troisième destination touristique préférée des Français aux États-Unis, la ville est célèbre pour le pont du Golden Gate, l'île et ancienne prison d'Alcatraz, Fisherman's Wharf, la Transamerica Pyramid, la Coit Tower, ses maisons victoriennes, ses cable cars et ses nombreuses collines découpées de rues en pente.
                </p>
           

                <p id="color" id='cuisine'>
                Les Californiens, qui ont toujours eu le culte du corps, pratiquent avec conviction le régime eating healthy (manger sain). 
                
                Fans de bio (organic), ilssont aussi de plus en plus adeptes de cuisine vegan. 
                
                Le phénomène « locavore » fait aussi partie des mœurs.

                Ce comportement alimentaire né à San Francisco en 2005 privilégie la consommation d'ingrédients locaux, pas nécessairement bio mais produits dans un rayon limité (en général, une centaine de miles). 
                
                L’équivalent de ce que l’on nomme circuit court en France.

                De plus en plus de restaurateurs affichent désormais fièrement la provenance de leurs produits, souvent des fermes des environs (le concept « Farm to table »), quand ils ne cultivent pas eux-mêmes leurs légumes dans leur propre potager ! 
                
                Bref, le produit, au sens noble et artisanal du terme, a le vent en poupe.

                Mais la grande particularité de la Californie (particulièrement dans les grandes villes comme San Francisco ou Los Angeles), c’est que toutes les cuisines sont représentées et qu’on trouve partout de tout à tous les prix, de la street food vendue par les marchands ambulants à la New American cuisine concoctée par des chefs inspirés, en passant par toutes les spécialités du monde entier.
                
                Reste que, si l’on peut manger sain et varié sur la côte, c’est en revanche moins le cas dans les grands espaces de la Californie intérieure, où le graillon des cow-boys reste encore la norme...
                </p>
            
            
                <p id="color" id='streetart'>
                À San Francisco, il n’y a pas besoin d’aller très loin pour en trouver. 
                
                Quasiment à chaque coin de rue, on peut tomber sur un graffiti. 
                
                Mais il y a un quartier où le street art règne en maître, c’est Mission District. 
                
                C’est presque difficile de trouver un mur “nu” !
                </p>
           
            
